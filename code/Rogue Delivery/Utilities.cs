        public static char BrailleFor(ICollection<Coord> coords) {
            if (coords is null) {
                throw new ArgumentNullException(nameof(coords));
            }
            int b = 0x2800;
            foreach (Coord c in coords) {
                if (c.X == 0) {
                    switch (c.Y) {
                        case 0:
                            b += 0x1;
                            break;
                        case 1:
                            b += 0x2;
                            break;
                        case 2:
                            b += 0x4;
                            break;
                        case 3:
                            b += 0x40;
                            break;
                    }
                } else if (c.X == 1) {
                    switch (c.Y) {
                        case 0:
                            b += 0x8;
                            break;
                        case 1:
                            b += 0x10;
                            break;
                        case 2:
                            b += 0x20;
                            break;
                        case 3:
                            b += 0x80;
                            break;
                    }
                }
            }
            return (char)b;
        }

        public static string CapitalizeFirst(string original) {
            if (string.IsNullOrWhiteSpace(original)) {
                return "";
            }
            return original[0].ToString().ToUpper() + (original.Length > 1 ? original.Substring(1) : "");
        }

        // Replaces all white space with a single space
        public static string Caps(string original) {
            if (string.IsNullOrWhiteSpace(original)) {
                return "";
            }
            return original.Split((char[])null)
                .Select(s => CapitalizeFirst(s))
                .Aggregate((i, j) => i + ' ' + j);
        }

        /**
         * Provides a string full of lines appropriate for the direction. If a stable set is desired, using the first
         * character from the set returned will work nicely.
         */
        public static string LinesFor(Direction dir) {
            switch (dir) {
                case Direction.Down:
                case Direction.Up:
                    return "|｜∣ǀ";
                case Direction.DownLeft:
                case Direction.UpRight:
                    return "/／╱⁄";
                case Direction.DownRight:
                case Direction.UpLeft:
                    return "\\＼╲";
                case Direction.Left:
                case Direction.Right:
                    return "-－−‐‑‒–—―";
                case Direction.None:
                default:
                    return "+＋✚✕✖✗";
            }
        }

        /**
         * Provides a string full of arrows appropriate for the direction. If a stable set is desired, using the first
         * character from the set returned will work nicely.
