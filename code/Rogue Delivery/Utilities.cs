        public static Coord FindCentroid(List<Coord> coords) {
            if (coords is null) {
                throw new ArgumentNullException(nameof(coords), "List of Coord must not be null.");
            }
            if (coords.Count == 0) {
                throw new ArgumentException("Must have at least one Coord.");
            }
            int centroidX = 0;
            int centroidY = 0;
            foreach (Coord c in coords) {
                centroidX += c.X;
                centroidY += c.Y;
            }
            centroidX /= coords.Count;
            centroidY /= coords.Count;
            return Coord.Get(centroidX, centroidY);
        }

